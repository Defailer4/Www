public class Geeks {
    public static void main(String[] args) {

        // initializing array
        int[] arr = { 40,55,63,17,22,68};

        // size of array
        int n = arr.length;

        // traversing array
        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");
    }
}